    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        Atendente obj = new Atendente();
        obj.setCodigo(Long.parseLong(txtCodigo.getText()));
        obj.setNome(txtNome.getText());
        obj.setEmail(txtEmail.getText());
        obj.setSenha(psswdSenha.getText());
        
        AtendenteDAO dao = new AtendenteDAO();
        if(dao.alterar(obj)> 0){
            JOptionPane.showMessageDialog(null,"Registro alterado com sucesso");
        }
        else{
            JOptionPane.showMessageDialog(null,"Ocorreu um erro, verifique!");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        Atendente obj = new Atendente();
        obj.setCodigo(Long.parseLong(txtCodigo.getText()));
        AtendenteDAO dao = new AtendenteDAO();
        if(dao.remover(obj)> 0){
            JOptionPane.showMessageDialog(null,"Registro removido com sucesso !");
        }
        else{
            JOptionPane.showMessageDialog(null,"Ocorreu um erro, verifique!");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String codigo = txtCodigo.getText();
        AtendenteDAO dao = new AtendenteDAO();
        Atendente obj = dao.retornaAtendente(codigo);
        if(obj.getNome() == null){
            txtNome.setText("");
            txtEmail.setText("");
            psswdSenha.setText("");
        }
        else{
            txtNome.setText(obj.getNome());
            txtEmail.setText(obj.getEmail());
            psswdSenha.setText(obj.getSenha());
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

