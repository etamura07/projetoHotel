    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Servico obj = new Servico();
        obj.setDescritivo(txtDescritivo.getText());
        obj.setTipo(txtTipo.getText());
        obj.setValor(Double.parseDouble(txtValor.getText()));
        obj.setData(txtData.getText());
        ServicoDAO dao = new ServicoDAO();
        if(dao.cadastrar(obj) == true){
            JOptionPane.showMessageDialog(null, "Registro inserido com sucesso");
        }
        else{
            JOptionPane.showMessageDialog(null,"Ocorreu um erro, verifique!");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

