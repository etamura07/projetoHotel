    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        Cliente obj = new Cliente();
        obj.setCodigo(Long.parseLong(txtCodigo.getText()));
        obj.setNome(txtNome.getText());
        obj.setDataNascimento(txtDataNasc.getText());
        obj.setCpf(txtCPF.getText());
        obj.setLogradouro(txtLogradouro.toString());
        obj.setBairro(txtBairro.toString());
        obj.setCidade(txtCidade.toString());
        obj.setEstado(txtEstado.toString());
        obj.setTelefone(txtTelefone.toString());
        obj.setCep(txtCep.toString());
        
        ClienteDAO dao = new ClienteDAO();
        if(dao.alterar(obj)> 0){
            JOptionPane.showMessageDialog(null,"Registro alterado com sucesso");
        }
        else{
            JOptionPane.showMessageDialog(null,"Ocorreu um erro, verifique!");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        Cliente obj = new Cliente();
        obj.setCodigo(Long.parseLong(txtCodigo.getText()));
        ClienteDAO dao = new ClienteDAO();
        if(dao.remover(obj)> 0){
            JOptionPane.showMessageDialog(null,"Registro removido com sucesso !");
        }
        else{
            JOptionPane.showMessageDialog(null,"Ocorreu um erro, verifique!");
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String codigo = txtCodigo.getText();
        ClienteDAO dao = new ClienteDAO();
        Cliente obj = dao.retornaCliente(codigo);
        if(obj.getNome() == null){
            txtNome.setText("");
            txtDataNasc.setText("");
            txtCPF.setText("");
            txtLogradouro.setText("");
            txtBairro.setText("");
            txtCidade.setText("");
            txtEstado.setText("");
            txtTelefone.setText("");
            txtCep.setText("");
        }
        else{
            txtNome.setText(obj.getNome());
            txtDataNasc.setText(obj.getDataNascimento());
            txtCPF.setText(obj.getCpf());
            txtLogradouro.setText(obj.getLogradouro());
            txtBairro.setText(obj.getBairro());
            txtCidade.setText(obj.getCidade());
            txtEstado.setText(obj.getEstado());
            txtTelefone.setText(obj.getTelefone());
            txtCep.setText(obj.getCep());
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

